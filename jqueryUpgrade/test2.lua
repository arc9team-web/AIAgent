따라서 기존 `sticky-kit.min.js` 파일의 **로드 스크립트를 과감하게 주석 처리**하시고, 기존에 `.stick_in_parent()` 함수가 적용되던 플로팅 메뉴나 사이드바 클래스에 **`position: sticky;` CSS를 부여**하는 방향으로 리팩토링하시는 것을 강력하게 추천합니다. 

이 작업을 하시면 jQuery 4.0 업그레이드 충돌을 피할 수 있을 뿐만 아니라, **웹사이트의 스크롤 퍼포먼스가 눈에 띄게 쾌적해지는 효과**를 얻으실 수 있습니다!